package cl.losheroes.cargasfamiliares.dto;

public class ResumenCargasFamiliares {

	protected String tramo;
	protected String cantTrab;
	protected String cantCargas;
	protected String cantCargVenc;

	public String getTramo() {
		return tramo;
	}

	public void setTramo(String tramo) {
		this.tramo = tramo;
	}

	public String getCantTrab() {
		return cantTrab;
	}

	public void setCantTrab(String cantTrab) {
		this.cantTrab = cantTrab;
	}

	public String getCantCargas() {
		return cantCargas;
	}

	public void setCantCargas(String cantCargas) {
		this.cantCargas = cantCargas;
	}

	public String getCantCargVenc() {
		return cantCargVenc;
	}

	public void setCantCargVenc(String cantCargVenc) {
		this.cantCargVenc = cantCargVenc;
	}
}
